// Include phoenix_html to handle method=PUT/DELETE in forms and buttons.
import "phoenix_html"
// Establish Phoenix Socket and LiveView configuration.
import {Socket} from "phoenix"
import {LiveSocket} from "phoenix_live_view"
import topbar from "../vendor/topbar"
import GanttChart from "./hooks/gantt_hook"

const csrfToken = document.querySelector("meta[name='csrf-token']").getAttribute("content")
const liveSocket = new LiveSocket("/live", Socket, {
  longPollFallbackMs: 2500,
  params: {_csrf_token: csrfToken},
  hooks: { GanttChart },
})

// Show progress bar on live navigation and form submits
topbar.config({barColors: {0: "#29d"}, shadowColor: "rgba(0, 0, 0, .3)"})
window.addEventListener("phx:page-loading-start", _info => topbar.show(300))
window.addEventListener("phx:page-loading-stop", _info => topbar.hide())

// Keyboard shortcuts (only when not focused on an input/textarea)
document.addEventListener("keydown", (e) => {
  const tag = document.activeElement?.tagName
  if (tag === "INPUT" || tag === "TEXTAREA" || document.activeElement?.isContentEditable) return
  if (e.metaKey || e.ctrlKey || e.altKey) return

  if (e.key === "/") {
    e.preventDefault()
    const searchInput = document.querySelector('[data-shortcut="search"]')
    if (searchInput) {
      searchInput.focus()
    } else {
      window.location.href = "/search"
    }
  } else if (e.key === "n") {
    e.preventDefault()
    const newTaskInput = document.querySelector('[data-shortcut="new-task"]')
    if (newTaskInput) newTaskInput.focus()
  } else if (e.key === "e") {
    e.preventDefault()
    const selectedTask = document.querySelector('[data-selected="true"]')
    if (selectedTask) {
      const editBtn = selectedTask.querySelector('[data-shortcut="edit-task"]')
      if (editBtn) editBtn.click()
    }
  } else if (e.key === "Escape") {
    document.activeElement?.blur()
  }
})

// connect if there are any LiveViews on the page
liveSocket.connect()

// expose liveSocket on window for web console debug logs and latency simulation:
// >> liveSocket.enableDebug()
// >> liveSocket.enableLatencySim(1000)  // enabled for duration of browser session
// >> liveSocket.disableLatencySim()
window.liveSocket = liveSocket

// The lines below enable quality of life phoenix_live_reload
// development features:
//
//     1. stream server logs to the browser console
//     2. click on elements to jump to their definitions in your code editor
//
if (process.env.NODE_ENV === "development") {
  window.addEventListener("phx:live_reload:attached", ({detail: reloader}) => {
    // Enable server log streaming to client.
    // Disable with reloader.disableServerLogs()
    reloader.enableServerLogs()

    // Open configured PLUG_EDITOR at file:line of the clicked element's HEEx component
    //
    //   * click with "c" key pressed to open at caller location
    //   * click with "d" key pressed to open at function component definition location
    let keyDown
    window.addEventListener("keydown", e => keyDown = e.key)
    window.addEventListener("keyup", _e => keyDown = null)
    window.addEventListener("click", e => {
      if(keyDown === "c"){
        e.preventDefault()
        e.stopImmediatePropagation()
        reloader.openEditorAtCaller(e.target)
      } else if(keyDown === "d"){
        e.preventDefault()
        e.stopImmediatePropagation()
        reloader.openEditorAtDef(e.target)
      }
    }, true)

    window.liveReloader = reloader
  })
}


// Allows to execute JS commands from the server
window.addEventListener("phx:js-exec", ({detail}) => {
  document.querySelectorAll(detail.to).forEach(el => {
    liveSocket.execJS(el, el.getAttribute(detail.attr))
  })
})
